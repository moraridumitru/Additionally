/**It is necessary to write a program that would display asterisks in the console in this way, as stars below.
 * * * * *
 * * * * *
 * * * * *
 */

public class Loop_for_5 {
    public static void main(String[] args){

        for(int i=0; i<3; i++){
            for(int j=0; j<5; j++){
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}

